message = "Hello Python world!"
print(message)
# The variable message is assigned the string value "Hello Python world!".
# The print() function then outputs the value of message, which is Hello Python world!.


message = "Hello Python Crash Course world!"
print(message)
# The same variable message is reassigned a new string value: "Hello Python Crash Course world!".
# The print() function now outputs the updated value of message, which is Hello Python Crash Course world!.


''' 
Key Concepts:

Variables: message is a variable that stores string values. You can change (reassign) its value anytime.

print(): A built-in Python function that displays the value passed to it (in this case, the variable message).

Dynamic Typing: Python allows variables to be reassigned to different data types, though here it's just strings.
'''